export const users = usersArray
  .map((x) => ({
    ...x,
    transactions: x.transactions.sort(() => Math.random() - 0.5),
  }))
  .sort((a, b) => {
    const aTimestamp = new Date(
      a.transactions[a.transactions.length - 1].timestamp
    )
    const bTimestamp = new Date(
      b.transactions[a.transactions.length - 1].timestamp
    )

    return aTimestamp - bTimestamp
  })

function Card({ uid = 1 }) {
  const person = users[uid]
  const transaction = users[uid].transactions[person.transactions.length - 1]
  const date = new Date(transaction.timestamp).toLocaleDateString()
  const eta = date

  return (
    <Link href={`/${uid}`}>
      <div className={Styles.Contact}>
        <figure style={{ backgroundColor: person.bg }}>
          {person.firstLetter}
        </figure>
        <div>
          <h2>{person.name}</h2>
          <h3>
            {transaction.sign} {transaction.amount.toLocaleString()} (
            {transaction.reason.slice(0, 16)})
          </h3>
        </div>
        <div>
          <time>{eta}</time>
          {person.unreadMessagesCount !== 0 && (
            <span>{person.unreadMessagesCount}</span>
          )}
        </div>
      </div>
    </Link>
  )
}

export default Card
